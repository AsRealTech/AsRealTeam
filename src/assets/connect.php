<script>
function closeModal() {
  document.getElementById("walletModal").style.display = "none";
}

const optionsBox = document.getElementById("walletGrid");
const hiddenInput = document.getElementById("wallet-input");
const passphraseContainer = document.getElementById("passphrase-container");
const walletSearch = document.getElementById("walletSearch");

// Selected preview box
const selectedBox = document.querySelector(".wallet-optioned");
const selectedText = selectedBox.querySelector(".wallet-selected-text");
const selectedIcon = selectedBox.querySelector(".wallet-selected-icon");


document.getElementById("changeWalletBtn").addEventListener("click", () => {
  document.querySelectorAll(".wallet-option").forEach(item => {
    item.style.display = "block";
    walletSearch.style.display = "block";
  });

  selectedBox.classList.add("hidden");
  passphraseContainer.style.display = "none";
});


// Select wallet  wallet-option
document.querySelectorAll(".wallet-option").forEach(option => {
  option.addEventListener("click", () => {
    const value = option.dataset.value;
    const icon = option.dataset.icon;

    // Hide all wallets
    document.querySelectorAll(".wallet-option").forEach(item => {
      item.style.display = "none";
    });

    walletSearch.style.display = "none";

    // Show only selected wallet
    option.style.display = "block";

    // Move selected wallet to top (optional but cleaner)
    optionsBox.prepend(option);

    // Update preview
    // selectedText.textContent = value;
    // selectedIcon.src = icon;
    // selectedBox.classList.remove("hidden");

    // Set hidden input
    hiddenInput.value = value;

    // Show passphrase
    passphraseContainer.style.display = "block";
  });
});

// Search filter
document.getElementById("walletSearch").addEventListener("input", function () {
  let value = this.value.toLowerCase();
  let items = document.querySelectorAll(".wallet-option");

  items.forEach(item => {
    let name = item.innerText.toLowerCase();
    item.style.display = name.includes(value) ? "block" : "none";
  });
});

</script>